// Sort the secondary batch alphabetically for quick scanning.
SECONDARY.sort((a, b) => a.name.localeCompare(b.name));

export const COUNTRY_OPTIONS: CountryOption[] = [...PRIMARY, ...SECONDARY];

const CODE_SET = new Set(COUNTRY_OPTIONS.map((c) => c.code));

export function isKnownCountryCode(code: string): boolean {
  return CODE_SET.has(code);
}

export function countryName(code: string): string {
  return COUNTRY_OPTIONS.find((c) => c.code === code)?.name ?? code;
}
